<p><strong>DM in Nürnberg: DLV bezuschusst Vereinsbusse zu den Titelkämpfen</strong></p>

<p>1. April 2015 (blv-sport.de) - DLV-Präsident Dr. Clemens Prokop bezeichnet die Deutschen Meisterschaften im Nürnberger Grundig-Stadion als Herausforderung für alle Leichtathletik-Organisationen. In einem Brief an alle Landesverbände, Bezirke, Kreise und Vereine appelliert der aus Saal/Donau stammende DLV-Präsident, die Titelkämpfe vom 24. bis 26. Juli zu einem Erfolg für die gesamte deutsche Leichtathletik-Familie werden zu lassen. Der DLV bezuschusst deshalb auch Busse von Vereinen und Verbänden, die nach Nürnberg reisen. <a href="http://lg-telis-finanz.de/2015_ausschreibungen/Brief_DLV-Praesident_DM-Karten.pdf" target="_blank">Hier</a> geht's weiter zum brief des DLV-Präsidenten ...</p>


<p><strong>Leichtathletik-DM in Nürnberg: Ab sofort gibt es auch Tickets im Oberrang</strong></p>

<p>Regensburg, 28. März 2015 (blv-sport.de) - Der Ticket-Vorverkauf für die Deutschen Leichtathletik-Meisterschaften am 25./26. Juli im Nürnberger Grundig-Stadion läuft so gut, dass sich die Veranstalter entschlossen haben, kurzfristig auch Zuschauerblöcke im Oberrang der Haupttribüne in den Vorverkauf zu geben. Ursprünglich war geplant, in dem 46 000-Zuschauer-Stadion nur Plätze in den unteren Rängen zu verkaufen. Der bisherige Vorverkauf deutet aber auf ein sehr großes Interesse der Sportfans für die nationalen Titelkämpfe hin. Bisher wurden für beide Wettkampftage zusammen bereits mehr als 20 000 Tickets im Vorverkauf abgesetzt. Inzwischen gibt es keine Plätze der Kategorie 1 Premium im Unterrang der Zielgeraden mehr. Deshalb haben sich die Organisatoren entschlossen, ab sofort auch Tickets für die darüber liegenden Zuschauerblöcke 36 und 40 zu verkaufen. Diese Top-Tickets kosten als Tageskarte 29,50 Euro und als Dauerkarte für beide Tage 55 Euro. <a href="http://blv-sport.de/index.php?id=377&tx_ttnews[tt_news]=2016&cHash=92ce17e3257d7c7f03ecae71eb6659b4" target="_blank">Hier</a> geht's weiter auf blv-sport.de ...</p> 


