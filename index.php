<h2>Leichtathletik-EM in Zürich</h2>

<p><strong>Täglich Highlights aus Sport und Show – Reisekosten inbegriffen</strong></p>

<p><img class="float_left" title="Stadion" src="/images/inhalt/stadion_stimmungsbild.jpg" height="180px" >Unterstützte die deutschen Athleten, wenn an den Europameisterschaften (12. bis 17. August) im legendären Letzigrund-Stadion die Leichtathletik auf einzigartige Weise zelebriert wird. Für alle Tage sind noch Eintrittskarten erhältlich. Die in den Tickets inbegriffene Anreise mit dem öffentlichen Verkehr ab Schweizer Grenze macht den Besuch für deutsche Fans besonders attraktiv. Preiswerte Unterkünfte können so auch außerhalb Zürichs gebucht werden, ohne das zusätzliche Reisekosten entstehen.<p>

<p>Mit 1.400 Athletinnen und Athleten aus 50 Nationen ist Zürich 2014 das grösste Sportereignis aller Zeiten für die Gastgeber-Nation. Das legendäre Letzigrund Stadion ist der Hauptschauplatz, unterhaltsame Shows beschliessen täglich das Wettkampfprogramm der besten Athleten Europas. Die Strassenrennen im Gehen und Marathon werden auf attraktiven Strecken im Zentrum der Metropole am Zürisee ausgetragen.</p>

<p>Und das City Festival zur EM mit vielen Attraktionen, Aktivitäten und Gewinnspielen findet ebenfalls in der Stadtmitte auf dem Sechseläutenplatz vor der imposanten Kulisse des Opernhauses statt. Im Mittelpunkt steht das eigens aufgebaute House of Switzerland, das zuletzt als Repräsentanz der Schweiz bei den Olympischen Winterspielen in Sotschi für Furore sorgte. Hier und im Stadion sind an allen EM-Tagen Höhepunkte aus Sport und Unterhaltung zu erleben.</p>

<p>Zuschauern aus Deutschland wird die Anreise leicht gemacht: Die Nutzung aller öffentlichen Verkehrsmittel ab Grenzübergang in die Schweiz (z. B. Schaffhausen, Basel, Buchs/St. Gallen) – egal ob per Bahn, Bus, Schiff oder Strassenbahn – ist an den Geltungstagen in der Eintrittskarte bereits enthalten. So sind für echte Leichtathletik-Fans auch preiswerte Unterkünfte jenseits der Grenze noch am Abend praktisch ohne weitere Kosten erreichbar.</p>

