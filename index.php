<p><strong>Paul Paszyna zum neuen Präsident der Pfalz gewählt</strong></p>

<p>27. April 2015 - Im Rahmen des Verbandstages haben die Delegierten der Pfälzer Leichtathletikvereine in Grünstadt ihren Präsidenten neu gewählt. Paul Paszyna, der bisherige Vizepräsident Allgemeine Leichtathletik, löste den bisherigen Präsidenten, Dr. Ulrich Becker, der zuvor 15 Jahre im Präsidium tätig war, darunter 12 als Präsident, ab. Paul Paszyna, auch Schatzmeister des Süddeutschen Leichtathletik-Verbandes, konnte mit einem neuen Team bei den Delegierten Punkten.</p>

