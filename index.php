    <h2>Eigener Verband gegründet</h2>

    <p>
      <strong>Die Landesverbände Bayern, Baden und Württemberg machen sich selbständig</strong>
    </p>

    <p><img class="float_left" src="images/inhalt/Verbandsgruendung.jpg" alt="Verbandsgründung" height="150px"/> Am Rande der traditionellen Ostertagung des Süddeutschen Leichtathletik-Verbandes kam es zu einem Paukenschlag, den so niemand erwartet hätte. Weil sie mit der aktuellen Entwicklung des DLV nicht zufrieden zu sein schienen, ergriffen die Präsidenten der Landesverbände Bayern, Baden und Württemberg mit ihrem Vorstoß die Initiative, aus dem Bundesverband austreten zu wollen. Gleichzeitig forderten sie die anwesenden Kolleginnen und Kollegen auf, es ihnen gleichzutun, denn die süddeutschen Verbände würden als eigene Vereinigung laut BLV-Präsident Philipp Krämer durchaus eine Macht darstellen, da sie ja doch etwas mehr als die Hälfte der Mitglieder des nationalen Verbandes auf sich vereinigen können. Begeistert von der Idee eines eigenen Verbandes zeigte sich SLV-Präsident Jochen Schweitzer, der hinter verschlossenen Türen mit den anwesenden Vertetern der Landesverbände, welche sich nocht nicht gleich zu diesem mutigen Schritt überreden lassen konnten, weiterverhandelte. Man kam zu dem Schluss, dass beitrittswillige Kandidaten bis zum 01.04.2016 außerordentliche Verbandstage zur Befragung ihrer Mitglieder/ Delegierten abhalten müssten, damit zu diesem Stichtag eine Meldung bei der EAA sowie auch der IAAF rechtzeitig bis um 23:59 Uhr eingehen kann. Nur so kann man laut Schweitzer auch gewährleisten, dass es mit einem Start bei den Olympischen Spielen in Rio noch klappen könnte. Das Team um die Präsidenten Wolfgang Schoeppe, Jürgen Scholz und Philipp Krämer ist überzeugt davon, eine schlagkräftige Mannschaft an den Zuckerhut schicken zu können. Starten wird man in diesem Jahr unter der württembergischen Flagge, was sich jedoch jährlich, wie auch die Präsidentschaft, ändern soll. Einen Beigeschmack dürfte das Vorhaben der südlichen Verbände dennoch für den DLV haben: So verliert dieser nicht nur mehr als die Hälfte seiner Mitglieder, sondern es droht auch der Umstand, dass bei einem Beitritts Hessens der nationale Verband seinen Sitz im Ausland haben würde. Wie man mit dieser neuen Situation umgehen werde, konnte man noch nicht in Erfahrung bringen, da sich DLV-Präsident Prokop derzeit noch im Osterurlaub befindet.
    </p>

