      <strong>Süddeutscher Leichtathletik-Verband vergrößert sich: LV Rheinland tritt Deutschlands größter Regionalvereinigung bei</strong>
    </p>


    <p>
      Der Süddeutsche Leichtathletik-Verband, welcher mit über 470.000 Mitgliedern in
rund 4400 Vereinen Deutschlands größte Regionalvereinigung der Leichtathletik
darstellt, erhält zum 01.01.2017 durch den Landesverband Rheinland ein weiteres
Mitglied.
    </p>

    <p>
      „Der Beitritt zeigt, dass wir mit unserer Arbeit, die Leichtathletik innovativ, modern
und athletenfreundlich zu gestalten, richtig liegen“, so Jochen Schweitzer, welcher
dem SLV als Präsident vorsteht und sich gemeinsam mit seinem Präsidium über den
Zuwachs freut.
    </p>

    <p>
      Der SLV wurde 1913 vom Frankfurter Verband für Turnsport, dem Norbayerischen
Landesverband für Leichtathletik, dem Münchner Rasensportverband, dem
Württembergischen Landesverband für Leichtathletik sowie dem Verband
südwestdeutscher Athletikvereine gegründet und wird seit Ende des zweiten
Weltkriegs als Regionalvereinigung
weitergeführt. Ihm gehören
bisher die
Landesverbände Hessen, Rheinhessen, Pfalz, Saarland, Baden, Württemberg und
Bayern an.
    </p>

